package sentenciaControl;

public class EjemploIfElseIfElse {
    
    public static void main(String[] args) {
        
        int x = 10;
        
        if (x == 10) {
            System.out.println("X es igual a 10");
        } else if (x == 20) {
            System.out.println("X es igual a 20");
        } else if (x == 30) {
            System.out.println("X es igual a 30");
        } else {
            System.out.println("X no es igual a 10, 20 o 30");
        }
    }
}
